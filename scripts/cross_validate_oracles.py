#!/usr/bin/env python3
"""Cross-validate agent-generated oracles against existing ground truth.

Compares oracle_answer_agent.json / ground_truth_agent.json files
generated by context_retrieval_agent.py against the existing
Sourcegraph-generated oracle_answer.json and hand-curated
ground_truth.json files.

Reports:
- Per-task file-level F1 (using oracle_checks matching logic)
- Cohen's kappa for file-level agreement
- Per-suite breakdown
- Divergence analysis (SG-only vs agent-only files)

Usage:
    # Compare all tasks that have both agent and existing oracles
    python3 scripts/cross_validate_oracles.py

    # Compare specific suite
    python3 scripts/cross_validate_oracles.py --suite ccb_mcp_crossrepo_tracing

    # Compare from external directory
    python3 scripts/cross_validate_oracles.py --agent-dir results/agent_oracles/

    # Output report
    python3 scripts/cross_validate_oracles.py --report reports/cross_validation.json
"""

import argparse
import json
import logging
import re
import sys
from collections import defaultdict
from pathlib import Path
from typing import Any, Dict, List, Optional, Set, Tuple

log = logging.getLogger("cross_validate_oracles")

# ---------------------------------------------------------------------------
# Repo name normalization (mirroring oracle_checks.py logic)
# ---------------------------------------------------------------------------

_MIRROR_HASH_RE = re.compile(r"--(v[\d.]+|[0-9a-f]{6,})$")


def _normalize_repo(name: str) -> str:
    """Normalize a repo name for comparison.

    Strips org prefix and mirror hash suffix:
      sg-evals/firefox--871325b8 -> firefox
      openjdk/jdk -> jdk
      kubernetes/client-go -> client-go
    """
    base = name.rsplit("/", 1)[-1]
    base = _MIRROR_HASH_RE.sub("", base)
    return base.lower()


def _file_key(entry: Dict[str, str]) -> Tuple[str, str]:
    """Extract (normalized_repo, path) key from a file entry."""
    repo = entry.get("repo", "")
    path = entry.get("path", "")
    return (_normalize_repo(repo), path)


def _coerce_file_entry(entry) -> Optional[Dict[str, str]]:
    """Coerce various file entry formats to {repo, path} dict."""
    if isinstance(entry, dict):
        return entry
    if isinstance(entry, str):
        # Handle "sg-evals/repo/path/to/file.go" format
        parts = entry.split("/", 2)
        if len(parts) >= 3:
            repo = f"{parts[0]}/{parts[1]}"
            path = parts[2]
            return {"repo": repo, "path": path}
        elif len(parts) == 2:
            return {"repo": parts[0], "path": parts[1]}
        else:
            return {"repo": "", "path": entry}
    return None


# ---------------------------------------------------------------------------
# File-level comparison
# ---------------------------------------------------------------------------


def compare_file_sets(
    oracle_files: List[Dict], agent_files: List[Dict]
) -> Dict[str, Any]:
    """Compare two sets of files using three-pass matching.

    Returns metrics dict with F1, recall, precision, and divergence lists.
    """
    # Coerce entries
    oracle_entries = [_coerce_file_entry(f) for f in oracle_files]
    agent_entries = [_coerce_file_entry(f) for f in agent_files]
    oracle_entries = [e for e in oracle_entries if e]
    agent_entries = [e for e in agent_entries if e]

    # Build key sets (normalized repo + exact path)
    oracle_keys = {_file_key(e) for e in oracle_entries}
    agent_keys = {_file_key(e) for e in agent_entries}

    # Pass 1: exact match on normalized keys
    matched = oracle_keys & agent_keys
    oracle_remaining = oracle_keys - matched
    agent_remaining = agent_keys - matched

    # Pass 2: path-only fallback for unmatched
    oracle_by_path = {k[1]: k for k in oracle_remaining}
    agent_by_path = {k[1]: k for k in agent_remaining}
    path_matched_oracle = set()
    path_matched_agent = set()
    for path in set(oracle_by_path.keys()) & set(agent_by_path.keys()):
        path_matched_oracle.add(oracle_by_path[path])
        path_matched_agent.add(agent_by_path[path])

    oracle_remaining2 = oracle_remaining - path_matched_oracle
    agent_remaining2 = agent_remaining - path_matched_agent

    # Pass 3: path-suffix fallback — handles monorepo staging paths
    # e.g., "pkg/apis/meta/v1/types.go" matches
    #        "staging/src/k8s.io/apimachinery/pkg/apis/meta/v1/types.go"
    suffix_matched_oracle = set()
    suffix_matched_agent = set()
    if oracle_remaining2 and agent_remaining2:
        for okey in list(oracle_remaining2):
            for akey in list(agent_remaining2):
                opath, apath = okey[1], akey[1]
                if (opath.endswith("/" + apath.rsplit("/", 1)[-1])
                        and (apath.endswith(opath) or opath.endswith(apath))):
                    suffix_matched_oracle.add(okey)
                    suffix_matched_agent.add(akey)
                    break

    total_matched = (len(matched) + len(path_matched_oracle)
                     + len(suffix_matched_oracle))
    n_oracle = len(oracle_keys)
    n_agent = len(agent_keys)

    recall = total_matched / n_oracle if n_oracle else 1.0
    precision = total_matched / n_agent if n_agent else 0.0
    f1 = (2 * recall * precision / (recall + precision)
          if (recall + precision) > 0 else 0.0)

    # Divergence
    oracle_only = oracle_remaining2 - suffix_matched_oracle
    agent_only = agent_remaining2 - suffix_matched_agent

    return {
        "recall": round(recall, 4),
        "precision": round(precision, 4),
        "f1": round(f1, 4),
        "n_oracle": n_oracle,
        "n_agent": n_agent,
        "n_matched": total_matched,
        "oracle_only": [{"repo": r, "path": p} for r, p in sorted(oracle_only)],
        "agent_only": [{"repo": r, "path": p} for r, p in sorted(agent_only)],
    }


# ---------------------------------------------------------------------------
# Task discovery
# ---------------------------------------------------------------------------


def find_project_root() -> Path:
    p = Path(__file__).resolve().parent
    while p != p.parent:
        if (p / "benchmarks").is_dir():
            return p
        p = p.parent
    return Path(__file__).resolve().parent.parent


def discover_comparison_pairs(
    suite: str = "",
    agent_dir: str = "",
    agent_suffix: str = "_agent",
) -> List[Dict[str, Any]]:
    """Find all tasks with both an existing oracle and an agent-generated oracle.

    Returns list of dicts with: task_dir, existing_path, agent_path, suite.
    """
    root = find_project_root()
    benchmarks = root / "benchmarks"
    pairs = []

    # Determine which suites to scan
    suites = []
    if suite:
        suites = [suite]
    else:
        for d in sorted(benchmarks.iterdir()):
            if d.is_dir() and d.name.startswith(("csb_", "ccb_")):
                suites.append(d.name)

    for s in suites:
        suite_dir = benchmarks / s
        if not suite_dir.is_dir():
            continue

        for task_dir in sorted(suite_dir.iterdir()):
            if not task_dir.is_dir():
                continue

            tests = task_dir / "tests"
            if not tests.is_dir():
                continue

            # Find existing oracle
            existing = None
            for name in ["oracle_answer.json", "ground_truth.json"]:
                p = tests / name
                if p.exists():
                    existing = p
                    break

            # Find agent oracle (in-place or in external dir)
            agent = None
            if agent_dir:
                ext_dir = Path(agent_dir) / s
                for pattern in [
                    f"{task_dir.name}_oracle{agent_suffix}.json",
                    f"{task_dir.name}_gt{agent_suffix}.json",
                ]:
                    p = ext_dir / pattern
                    if p.exists():
                        agent = p
                        break
            else:
                for name in [f"oracle_answer{agent_suffix}.json", f"ground_truth{agent_suffix}.json"]:
                    p = tests / name
                    if p.exists():
                        agent = p
                        break

            if existing and agent:
                pairs.append({
                    "task_dir": str(task_dir),
                    "task_name": task_dir.name,
                    "suite": s,
                    "existing_path": str(existing),
                    "agent_path": str(agent),
                    "existing_type": existing.name,
                })

    return pairs


def _extract_files(data: Dict[str, Any]) -> List[Dict]:
    """Extract file list from various oracle/ground_truth formats.

    Handles:
    - oracle_answer.json: {"files": [{"repo": ..., "path": ...}]}
    - ground_truth.json (fix/feature/refactor): {"files": ["path", ...], "repo": "..."}
    - ground_truth.json (debug/design/understand): {"file_references": [...]}
    - ground_truth.json (dependency_chain): {"dependency_chain": ["path", ...]}
    - ground_truth_agent.json: same as oracle_answer.json format
    """
    result = []

    # "files" key — could be dicts or plain strings
    if "files" in data and isinstance(data["files"], list):
        repo = data.get("repo", "")
        for f in data["files"]:
            if isinstance(f, dict):
                result.append(f)
            elif isinstance(f, str):
                result.append({"repo": repo, "path": f})
        if result:
            return result

    # "file_references" key (ccb_debug/design/understand)
    if "file_references" in data:
        refs = data["file_references"]
        for ref in refs:
            if isinstance(ref, dict):
                path = ref.get("file", ref.get("path", ""))
                if path:
                    result.append({"repo": "", "path": path})
            elif isinstance(ref, str):
                result.append({"repo": "", "path": ref})
        if result:
            return result

    # "root_cause_files" as additional files (ccb_fix)
    for key in ("root_cause_files", "dependency_chain"):
        items = data.get(key, [])
        if isinstance(items, list):
            repo = data.get("repo", "")
            for f in items:
                if isinstance(f, str):
                    entry = {"repo": repo, "path": f}
                    if entry not in result:
                        result.append(entry)
                elif isinstance(f, dict):
                    if f not in result:
                        result.append(f)

    return result


# ---------------------------------------------------------------------------
# Agreement statistics
# ---------------------------------------------------------------------------


def cohens_kappa(
    oracle_file_sets: List[Set[str]],
    agent_file_sets: List[Set[str]],
    all_files: Set[str],
) -> float:
    """Compute Cohen's kappa for file-level agreement.

    Each task provides two raters' binary judgments (included/not included)
    for each file in the universe.
    """
    # Build confusion matrix
    a = 0  # both agree present
    b = 0  # oracle yes, agent no
    c = 0  # oracle no, agent yes
    d = 0  # both agree absent

    for oracle_set, agent_set in zip(oracle_file_sets, agent_file_sets):
        for f in all_files:
            in_oracle = f in oracle_set
            in_agent = f in agent_set
            if in_oracle and in_agent:
                a += 1
            elif in_oracle and not in_agent:
                b += 1
            elif not in_oracle and in_agent:
                c += 1
            else:
                d += 1

    n = a + b + c + d
    if n == 0:
        return 1.0

    po = (a + d) / n
    pe = ((a + b) * (a + c) + (c + d) * (b + d)) / (n * n)

    if pe >= 1.0:
        return 1.0
    return (po - pe) / (1 - pe)


def _interpret_kappa(k: float) -> str:
    if k < 0:
        return "poor"
    elif k <= 0.20:
        return "slight"
    elif k <= 0.40:
        return "fair"
    elif k <= 0.60:
        return "moderate"
    elif k <= 0.80:
        return "substantial"
    else:
        return "almost_perfect"


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Cross-validate agent oracles against existing ground truth"
    )
    parser.add_argument(
        "--suite", type=str, default="",
        help="Only compare tasks in this suite",
    )
    parser.add_argument(
        "--agent-dir", type=str, default="",
        help="External directory with agent oracles (alternative to in-place)",
    )
    parser.add_argument(
        "--agent-suffix", type=str, default="_agent",
        help="Suffix for agent oracle filenames (default: '_agent'). "
             "E.g., '_agent' finds oracle_answer_agent.json and ground_truth_agent.json",
    )
    parser.add_argument(
        "--report", type=str, default="",
        help="Output report JSON path (default: results/cross_validation/summary.json)",
    )
    parser.add_argument(
        "--verbose", action="store_true",
    )
    args = parser.parse_args()

    logging.basicConfig(
        level=logging.DEBUG if args.verbose else logging.INFO,
        format="%(asctime)s %(levelname)s %(message)s",
    )

    pairs = discover_comparison_pairs(
        suite=args.suite, agent_dir=args.agent_dir,
        agent_suffix=args.agent_suffix,
    )

    if not pairs:
        log.error(
            "No comparison pairs found. Run context_retrieval_agent.py first "
            "to generate oracle_answer_agent.json / ground_truth_agent.json files."
        )
        return 1

    log.info("Found %d comparison pairs", len(pairs))

    # Compare each pair
    per_task = []
    per_suite = defaultdict(list)
    all_oracle_sets = []
    all_agent_sets = []
    all_files_universe = set()

    for pair in pairs:
        try:
            existing = json.loads(Path(pair["existing_path"]).read_text())
            agent = json.loads(Path(pair["agent_path"]).read_text())
        except (json.JSONDecodeError, OSError) as e:
            log.warning("Error reading %s: %s", pair["task_name"], e)
            continue

        existing_files = _extract_files(existing)
        agent_files = _extract_files(agent)

        if not existing_files and not agent_files:
            continue

        comparison = compare_file_sets(existing_files, agent_files)
        result = {
            "task": f"{pair['suite']}/{pair['task_name']}",
            "suite": pair["suite"],
            **comparison,
        }
        per_task.append(result)
        per_suite[pair["suite"]].append(result)

        # For kappa
        oracle_paths = {_file_key(e)[1] for e in existing_files if isinstance(e, dict)}
        agent_paths = {_file_key(e)[1] for e in agent_files if isinstance(e, dict)}
        all_oracle_sets.append(oracle_paths)
        all_agent_sets.append(agent_paths)
        all_files_universe.update(oracle_paths | agent_paths)

    if not per_task:
        log.error("No valid comparisons")
        return 1

    # Aggregate metrics
    mean_f1 = sum(r["f1"] for r in per_task) / len(per_task)
    mean_recall = sum(r["recall"] for r in per_task) / len(per_task)
    mean_precision = sum(r["precision"] for r in per_task) / len(per_task)

    kappa = cohens_kappa(all_oracle_sets, all_agent_sets, all_files_universe)

    # Per-suite summary
    suite_summary = {}
    for suite_name, results in sorted(per_suite.items()):
        suite_f1s = [r["f1"] for r in results]
        suite_summary[suite_name] = {
            "n": len(results),
            "mean_f1": round(sum(suite_f1s) / len(suite_f1s), 4),
            "min_f1": round(min(suite_f1s), 4),
            "max_f1": round(max(suite_f1s), 4),
        }

    # Divergence summary
    total_oracle_only = sum(len(r["oracle_only"]) for r in per_task)
    total_agent_only = sum(len(r["agent_only"]) for r in per_task)
    total_matched = sum(r["n_matched"] for r in per_task)

    # High-divergence tasks (F1 < 0.5)
    high_divergence = [r for r in per_task if r["f1"] < 0.5]
    high_divergence.sort(key=lambda r: r["f1"])

    report = {
        "summary": {
            "total_tasks": len(per_task),
            "mean_file_f1": round(mean_f1, 4),
            "mean_file_recall": round(mean_recall, 4),
            "mean_file_precision": round(mean_precision, 4),
            "cohens_kappa": round(kappa, 4),
            "kappa_interpretation": _interpret_kappa(kappa),
            "agent_suffix": args.agent_suffix,
        },
        "divergence": {
            "total_matched_files": total_matched,
            "total_oracle_only_files": total_oracle_only,
            "total_agent_only_files": total_agent_only,
        },
        "high_divergence": high_divergence,
        "per_suite": suite_summary,
        "per_task": per_task,
    }

    # Print summary
    print(f"\n{'=' * 60}")
    print("Oracle Cross-Validation Report")
    print(f"{'=' * 60}")
    print(f"Tasks compared: {len(per_task)}")
    print(f"Mean file F1:   {mean_f1:.4f}")
    print(f"Mean recall:    {mean_recall:.4f}")
    print(f"Mean precision: {mean_precision:.4f}")
    print(f"Cohen's kappa:  {kappa:.4f} ({_interpret_kappa(kappa)})")
    print(f"\nDivergence:")
    print(f"  Both agree:       {total_matched} files")
    print(f"  Oracle-only (SG): {total_oracle_only} files")
    print(f"  Agent-only:       {total_agent_only} files")
    print(f"\nPer-suite:")
    for s, m in sorted(suite_summary.items()):
        print(f"  {s}: n={m['n']}, F1={m['mean_f1']:.4f} [{m['min_f1']:.4f}-{m['max_f1']:.4f}]")

    if high_divergence:
        print(f"\nHigh-divergence tasks (F1 < 0.5): {len(high_divergence)}")
        for r in high_divergence[:10]:
            print(f"  {r['task']}: F1={r['f1']:.4f} (oracle={r['n_oracle']}, agent={r['n_agent']})")
        if len(high_divergence) > 10:
            print(f"  ... and {len(high_divergence) - 10} more")
    print(f"{'=' * 60}")

    # Write report (default: results/cross_validation/summary.json)
    report_path = args.report
    if not report_path:
        root = find_project_root()
        report_path = str(root / "results" / "cross_validation" / "summary.json")
    out = Path(report_path)
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(json.dumps(report, indent=2) + "\n")
    log.info("Report written: %s", out)

    return 0


if __name__ == "__main__":
    sys.exit(main())
